package org.riskfirst.same.same4j;

public class Same4JException extends RuntimeException {

	public Same4JException() {
		super();
	}

	public Same4JException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public Same4JException(String message, Throwable cause) {
		super(message, cause);
	}

	public Same4JException(String message) {
		super(message);
	}

	public Same4JException(Throwable cause) {
		super(cause);
	}

	
}
