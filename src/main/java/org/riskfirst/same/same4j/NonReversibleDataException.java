package org.riskfirst.same.same4j;

public class NonReversibleDataException extends RuntimeException {

	public NonReversibleDataException() {
		super();
	}

	public NonReversibleDataException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NonReversibleDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public NonReversibleDataException(String message) {
		super(message);
	}

	public NonReversibleDataException(Throwable cause) {
		super(cause);
	}

	
}
