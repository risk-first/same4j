package org.riskfirst.same.same4j;

public class Same4JDataException extends RuntimeException {

	public Same4JDataException() {
		super();
	}

	public Same4JDataException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public Same4JDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public Same4JDataException(String message) {
		super(message);
	}

	public Same4JDataException(Throwable cause) {
		super(cause);
	}

	
}
